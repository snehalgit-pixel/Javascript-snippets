/* Padding: padStart() and padEnd()
*/
let example = 'Gal Gadot';
console.log(example.padStart(10, 'a'));
console.log("\n");
console.log(example.padEnd(10, 'a'));
/* padStart() will pad the current string with another string, multiple times if needed, until the resulting string reaches the given length. padStart()-from the start of string and padEnd()-from the end of the string */